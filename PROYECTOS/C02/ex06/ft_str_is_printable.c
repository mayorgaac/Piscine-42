int	ft_str_is_printable(char *str)
{
	int	out;

	out = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*str)
	{
		if (*str > 31 && *str < 127)
		{
			out = 1;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (out);
}
